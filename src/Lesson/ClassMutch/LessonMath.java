package Lesson.ClassMutch;

public class LessonMath {
    public static void main(String[] args) {
        int s = -100;
        System.out.println(Math.abs(s));

        System.out.println(Math.PI);
        System.out.println(Math.max(Math.max(10, 99), Math.max(3, 133)));
        System.out.println(Math.min(1, 109));
        System.out.println(Math.pow(7, 2));
        System.out.println(Math.round(20.5));
        //округляет вверх
        System.out.println(Math.ceil(7.001));
        //округляет вниз
        System.out.println(Math.floor(7.99));
        System.out.println(Math.sqrt(9));


    }
}
